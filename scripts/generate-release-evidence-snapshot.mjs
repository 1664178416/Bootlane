async function readJson(relativePath) {
  return JSON.parse(await readFile(path.join(rootDir, relativePath), "utf8"));
}

async function readExisting(filePath) {
  try {
    return await readFile(filePath, "utf8");
  } catch (error) {
    if (error && typeof error === "object" && "code" in error && error.code === "ENOENT") {
      return "";
    }
    throw error;
  }
}

function scriptSource(name) {
  return scripts[name] ? `package.json scripts.${name}: \`${scripts[name]}\`` : "missing package script";
}

function sourceFor(entry) {
  return entry.sourceScript ? scriptSource(entry.sourceScript) : entry.source;
}

function packageRow(packageJson, label, keyEntry) {
  const access = packageJson.publishConfig?.access ?? "n/a";
  return `| \`${cell(label)}\` | \`${cell(packageJson.version)}\` | \`${String(packageJson.private ?? false)}\` | \`${cell(access)}\` | \`${cell(keyEntry)}\` |`;
}

function cell(value) {
  return String(value).replaceAll("|", "\\|");
}
