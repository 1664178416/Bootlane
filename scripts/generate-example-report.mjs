import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { pathToFileURL } from "node:url";
import { fileURLToPath } from "node:url";

const rootDir = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const fixturePath = path.join(rootDir, "examples", "fixtures", "node-missing-env");
const outputPath = path.join(rootDir, "examples", "reports", "node-missing-env.md");
const generatedAt = new Date("2026-06-08T00:00:00.000Z");
const checkMode = process.argv.includes("--check");
const coreEntryPath = path.join(rootDir, "packages", "core", "dist", "index.js");
const { analyzeProject, renderMarkdownReport } = await import(pathToFileURL(coreEntryPath).href);

await mkdir(path.dirname(outputPath), { recursive: true });

const report = await analyzeProject({
  targetPath: fixturePath,
  toolVersion: "0.1.0",
  now: generatedAt
});

const rendered = renderMarkdownReport(report).replace(
  `Target: \`${fixturePath}\``,
  "Target: `examples/fixtures/node-missing-env`"
);

const content = [
  "<!-- Generated by: pnpm example:report -->",
  "<!-- Source fixture: examples/fixtures/node-missing-env -->",
  "",
  rendered.trimEnd(),
  ""
].join("\n");

const existing = await readExisting(outputPath);
if (existing !== content) {
  if (checkMode) {
    throw new Error(`${path.relative(rootDir, outputPath)} is stale. Run pnpm example:report.`);
  }
  await writeFile(outputPath, content, "utf8");
  process.stdout.write(`updated ${path.relative(rootDir, outputPath)}\n`);
} else {
  process.stdout.write(`ok ${path.relative(rootDir, outputPath)}\n`);
}

async function readExisting(filePath) {
  try {
    return await readFile(filePath, "utf8");
  } catch (error) {
    if (error && typeof error === "object" && "code" in error && error.code === "ENOENT") {
      return "";
    }
    throw error;
  }
}
